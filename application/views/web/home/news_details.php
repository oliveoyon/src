                        <div class="entry_description">
                            <p>At the close of 2016 and to mark International Migrant’s Day (18 Dec), IFRC’s Secretary General, Elhadj As Sy, delivered a special message to children, asking them to put aside negative perceptions about people who migrate and instead, welcome them into their communities and treat them with kindness and understanding. Here’s his message: <br> “International Migrants Day is an important day to think about all the people in the world who have had to leave their homes, including people who left because of wars or disasters, because they no longer had food or water, or because they wanted a better life for their families. Many of them even took dangerous journeys across oceans and deserts to find safety.</p>
                            <p>“These people that you have heard so much about are just people. They are mums and dads, brothers and sisters, grandpas and grandmas. They are teachers, farmers and doctors. A lot of migrants are kids, just like you. <br> Many of them didn’t have a choice about leaving their homes. They were scared that, if they stayed, they would get hurt, or that their families might even be killed.”</p>
