                            <a href="#">Working with Reviewers</a>
                        </div>
                        <p class="description">Reviewers will help the editors to meet the deadlines of any project work. The Reviewers can help editors to work with reviewers, giving them the guidelines, information and support. Reviewers may take up their time with manuscripts to review that are of such poor quality they could be rejected articles on topics which are outside the reviewer's area of expertise. Reviewers generally like to know the final editorial decision of the paper, and see other reviewers’ comments.</p>
