                    <div class="entry-title">
                            <a href="#">Editorial Boards</a>
                        </div>
                        <p class="description">The editorial board, or (editorial) advisory board, is a team of experts in the journal's field. The members of editorial board will review submitted manuscripts. They will provide advice on journal policy and scope. They will try to identify topics for special issues. If they want, they may attract new authors and submissions and also act as advisers in the case of complex publishing ethics allegations.</p>
