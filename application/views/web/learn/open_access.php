                            <a href="#">Submission Systems</a>
                        </div>
                        <p class="description">All submissions to our journal will be managed using one of our online submission systems. Using an online submission system, one can access and process submitted papers from anywhere with internet access. Different access levels are available for different editorial roles and there are various help and training resources at one’s disposal to support them with these systems.</p>
